#[cfg(test)]
mod tests {
    use super::*;
    use ark_dsl::{Constraint, DataField, EntityDef, Expr, Item, TypeExpr};

    fn minimal_vehicle() -> ArkFile {
        let fuel = DataField {
            name: "fuel".into(),
            ty: TypeExpr::Named("Float".into()),
            constraint: Some(Constraint::Range(Expr::Number(0.0), Expr::Number(100.0))),
            default: None,
        };
        let priority = DataField {
            name: "priority".into(),
            ty: TypeExpr::Named("Int".into()),
            constraint: Some(Constraint::Range(Expr::Number(1.0), Expr::Number(10.0))),
            default: None,
        };
        // invariant: fuel >= 0
        let inv = Expr::BinOp(
            Box::new(Expr::Ident("fuel".into())),
            ">=".into(),
            Box::new(Expr::Number(0.0)),
        );
        let entity = EntityDef {
            name: "Vehicle".into(),
            inherits: vec![],
            data_fields: vec![fuel, priority],
            in_ports: vec![],
            out_ports: vec![],
            processes: vec![],
            invariants: vec![inv],
            temporals: vec![],
            description: None,
        };
        ArkFile {
            imports: vec![],
            items: vec![Item::Class(entity)],
            ..Default::default()
        }
    }

    #[test]
    fn translates_minimal_class() {
        let file = minimal_vehicle();
        let smt = to_smtlib(&file);
        assert!(smt.contains("(set-logic QF_LRA)"));
        assert!(smt.contains("(declare-const Vehicle_fuel Real)"));
        assert!(smt.contains("(declare-const Vehicle_priority Int)"));
        assert!(smt.contains("(assert (and (>= Vehicle_fuel 0.0) (<= Vehicle_fuel 100.0)))"));
        assert!(smt.contains("(assert (and (>= Vehicle_priority 1) (<= Vehicle_priority 10)))"));
        assert!(smt.contains("; invariant from Vehicle"));
        assert!(smt.contains("(push 1)"));
        assert!(smt.contains("(assert (not (>= Vehicle_fuel 0.0)))"));
        assert!(smt.contains("(check-sat)"));
        assert!(smt.contains("(pop 1)"));
        assert!(smt.trim_end().ends_with("(exit)"));
    }

    #[test]
    fn binop_and_unary_translate() {
        let expr = Expr::BinOp(
            Box::new(Expr::UnaryOp("not".into(), Box::new(Expr::Bool(false)))),
            "and".into(),
            Box::new(Expr::BinOp(
                Box::new(Expr::Ident("x".into())),
                "!=".into(),
                Box::new(Expr::Number(2.0)),
            )),
        );
        let s = expr_to_smt(&expr, "E");
        assert_eq!(s, "(and (not false) (distinct E_x 2.0))");
    }
}
